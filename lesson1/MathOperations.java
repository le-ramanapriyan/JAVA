public class MathOperations {
    public static void main(String[] args) {
        int x = 20;
        int y = 6;

        System.out.println("Addition: " + (x + y));        // 26
        System.out.println("Subtraction: " + (x - y));     // 14
        System.out.println("Multiplication: " + (x * y));  // 120
        System.out.println("Division: " + (x / y));        // 3
        System.out.println("Modulus: " + (x % y));         // 2
    }
}
